package ExemplosCap02;

/** ExemploComentarios:
Essa classe demonstra a utilização de variaveis em uma classe
em java. Sao declaradas variaveis do tipo int e double.
O exemplo tabem demonstra como imprimir o conteudo das variaveis na tela.
*/
public class Comentarios{
	public static void main(String[] args){
		int matricula = 1089, idade = 20; //declaracao de variaveis do tipo inteiro
		double dolar = 3.79;
		/*As linhas seguintes enviam o conteudo das
		variaveis para a tela*/
		System.out.println("Matricula: " + matricula + ", idade: " + idade + ", dolar: " + dolar);		
	}
}